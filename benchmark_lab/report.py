"""Markdown report generation from benchmark results.

Turns a results document into a human-readable Markdown summary: an environment
header followed by one table per workload, with latency, throughput, speedup,
memory, and — where applicable — GPU utilisation and ANN recall. The same
numbers feed PERFORMANCE.md.
"""

from __future__ import annotations

from typing import Any


def _fmt(value: Any) -> str:
    if isinstance(value, float):
        return f"{value:,.3g}" if value < 1000 else f"{value:,.0f}"
    if isinstance(value, int):
        return f"{value:,}"
    return str(value)


def _environment_block(env: dict[str, Any]) -> list[str]:
    lines = ["## Environment", ""]
    rows = [
        ("Platform", env.get("platform")),
        ("Processor", env.get("processor")),
        ("Logical cores", env.get("cpu_count_logical")),
        ("RAM (MB)", env.get("ram_total_mb")),
        ("Python", env.get("python")),
        ("NumPy", env.get("numpy")),
        ("NeuralForge-X", env.get("neuralforge")),
    ]
    gpu = env.get("gpu")
    if isinstance(gpu, dict):
        rows.append(("GPU", f"{gpu.get('name')} (cc {gpu.get('compute_capability')})"))
    lines += [f"- **{k}:** {v}" for k, v in rows if v is not None]
    lines.append("")
    return lines


def _workload_table(workload: dict[str, Any]) -> list[str]:
    baseline = workload["baseline"]
    multi_size = len(workload["sizes"]) > 1
    has_gpu = _any(workload, "gpu")
    has_recall = _any(workload, "recall_at_k")

    headers = (["size"] if multi_size else []) + ["backend", "best (ms)", "median (ms)"]
    headers += [f"speedup vs {baseline}", "throughput/s", "peak host (MiB)", "CPU%"]
    if has_gpu:
        headers.append("GPU%")
    if has_recall:
        headers.append("recall@k")

    lines = [
        f"### {workload['workload']}  ·  unit: {workload['unit']}",
        "",
        "| " + " | ".join(headers) + " |",
        "|" + "---|" * len(headers),
    ]

    for size in workload["sizes"]:
        ms = {m["backend"]: m for m in size["measurements"]}
        base_ms = ms.get(baseline, {}).get("best_ms")
        size_label = _size_label(size["params"])
        for m in size["measurements"]:
            speedup = f"{base_ms / m['best_ms']:.2f}×" if base_ms and m["best_ms"] > 0 else "—"
            row = ([size_label] if multi_size else []) + [
                m["backend"],
                _fmt(m["best_ms"]),
                _fmt(m["median_ms"]),
                speedup,
                _fmt(m["throughput_per_s"]),
                _fmt(m["peak_host_kib"] / 1024.0),
                _fmt(m["cpu_percent"]),
            ]
            if has_gpu:
                row.append(_fmt(m["gpu"]["util_mean_pct"]) if "gpu" in m else "—")
            if has_recall:
                row.append(_fmt(m["recall_at_k"]) if "recall_at_k" in m else "exact")
            lines.append("| " + " | ".join(row) + " |")
    lines.append("")
    return lines


def _size_label(params: dict[str, Any]) -> str:
    if "corpus" in params:
        return f"{params['corpus']:,}"
    if "dim" in params:
        return f"d={params['dim']}"
    return ", ".join(f"{k}={v}" for k, v in params.items())


def _any(workload: dict[str, Any], key: str) -> bool:
    return any(key in m for size in workload["sizes"] for m in size["measurements"])


def render_markdown(doc: dict[str, Any]) -> str:
    """Renders the full results document as a Markdown report."""
    lines = [
        "# NeuralForge-X — cross-stack benchmark report",
        "",
        "_Generated by `python -m benchmark_lab`. Numbers are best-of-N wall-clock "
        "on the machine below; reproduce locally._",
        "",
    ]
    lines += _environment_block(doc.get("environment", {}))
    for workload in doc.get("workloads", []):
        lines += _workload_table(workload)
    return "\n".join(lines).rstrip() + "\n"
